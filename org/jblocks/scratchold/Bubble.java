package org.jblocks.scratchold;

class Bubble {

	String contents;

	/**
	 * @return the contents of this bubble
	 */
	public String getContents() {
		return contents;
	}

	/**
	 * @param contents
	 *            the new contents of this bubble
	 */
	public void setContents(String contents) {
		this.contents = contents;
	}
}